#Operadores aritmeticos

#Num3 += Num4
#print(Num3)

Num3 = 4
Num4 = 2

res2 = Num3 + Num4

print(f"El resultado de sumar {Num3} + {Num4} = {res2}") #f-string

print("El resultado de sumar", Num3, "+", Num4, "=", res2)

print("El resultado de sumar {} + {} = {}".format(Num3, Num4, res2))

#+, -, **, /, //, %, *

Num3 = 4
Num4 = 2

res2 = Num3 % Num4

print(f"El resultado del modulo {Num3} % {Num4} = {res2}") #f-string

print("El resultado del modulo", Num3, "%", Num4, "=", res2)

print("El resultado del modulo {} % {} = {}".format(Num3, Num4, res2))

